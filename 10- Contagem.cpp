#include <iostream>  // Inclui a biblioteca padrão para entrada e saída

int main() {
    // Estrutura de repetição for para contar de 1 a 100
    for (int i = 1; i <= 100; ++i) {
        std::cout << i << std::endl;  // Imprime o valor de i na tela
    }

    return 0;  // Indica que o programa terminou com sucesso
}
